	<h2>My Work Philosophy</h2>
	<p>My personal design philosophy is to keep things simple, yet elegant; but I do like to add a little flashiness here and there. My main goal will always be to let the content communicate its messages effectively without the design getting in the way. During development I aim to work quickly and efficiently, but I try not to compromise on quality if I can help it. There is <a href="http://en.wikipedia.org/wiki/Project_management_triangle" target="_blank">a saying</a> about software development: "Fast, Good and Cheap: Pick Two " - meaning, those three properties are interrelated and you can only have two of the three in any project. Compromising on quality for the sake of speed doesn't make sense to me - in the end you'll just spend more time doing maintenance to fix rushed work that resulted in mistakes. I'd rather get it right the first time.</p>
 

